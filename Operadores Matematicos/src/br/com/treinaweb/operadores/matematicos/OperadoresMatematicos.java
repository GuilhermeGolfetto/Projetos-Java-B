package br.com.treinaweb.operadores.matematicos;

public class OperadoresMatematicos {

	public static void main(String[] args) {
		int i = 0;
		System.out.println("Valor de i = " + i);
		System.out.println("Valor de i++ = " + i++);
		System.out.println("Valor de i agora = " + i);
		System.out.println("Valor de ++i = " + ++i);
		System.out.println("Valor de i agora = " + i);
	}

}
